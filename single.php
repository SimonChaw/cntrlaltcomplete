<?php
/**
 * The template for displaying all single posts
 *
 * @link https://developer.wordpress.org/themes/basics/template-hierarchy/#single-post
 *
 * @package WordPress
 * @subpackage cntrlaltcomplete
 * @since 1.0.0
 */

get_header();
?>

	<section id="primary" class="content-area">
		<main id="main" class="site-main">
      <div class="bg-white my-6 mx-auto xl:w-3/4 lg:w-3/4 w-5/6 shadow-md px-4 py-4">
			<?php

			/* Start the Loop */
			while ( have_posts() ) :
				the_post();
        the_content();

				if ( is_singular( 'attachment' ) ) {
					// Parent post navigation.
					the_post_navigation(
						array(
							/* translators: %s: parent post link */
							'prev_text' => sprintf( __( '<span class="meta-nav">Published in</span><span class="post-title">%s</span>', 'cntrlaltcomplete' ), '%title' ),
						)
					);
				} else if ( is_singular( 'post' ) ) {
					// Previous/next post navigation.
					the_post_navigation(
						array(
							'next_text' => '<span class="meta-nav" aria-hidden="true">' . __( 'Next Post', 'cntrlaltcomplete' ) . '</span> ' .
								'<span class="screen-reader-text">' . __( 'Next post:', 'twentynineteen' ) . '</span> <br/>' .
								'<span class="post-title">%title</span>',
							'prev_text' => '<span class="meta-nav" aria-hidden="true">' . __( 'Previous Post', 'cntrlaltcomplete' ) . '</span> ' .
								'<span class="screen-reader-text">' . __( 'Previous post:', 'cntrlaltcomplete' ) . '</span> <br/>' .
								'<span class="post-title">%title</span>',
						)
					);
				}

				// If comments are open or we have at least one comment, load up the comment template.
				if ( comments_open() || get_comments_number() ) {
					comments_template();
				}

			endwhile; // End of the loop.
			?>
    </div>
		</main><!-- #main -->
	</section><!-- #primary -->

<?php
get_footer();
